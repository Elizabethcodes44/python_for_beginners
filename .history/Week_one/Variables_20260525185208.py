#Variables are like containers holding data. They can be used to store different types of data such as numbers, text, and more.
# Strings are a type of data that represent text. They are enclosed in quotes, either single (' ') or double (" ").
# For example:

name = "Alice"

# integers are whole numbers without a decimal point. They can be positive, negative, or zero. For example:
age = 30
# Floats are numbers that have a decimal point. They can also be positive, negative, or zero. For example:
height = 5.6

#type is used to tell us what type of data a variable is storing. We can use the type() function to check the type of a variable. For example:
print(type(name))  # Output: <class 'str'>

#boolean is a data type that can only have two values: True or False. They are often used in conditional statements to control the flow of a program. For example:
is_student = True

